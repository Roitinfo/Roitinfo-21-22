#include<iostream>
using namespace std;

int main()
{
	int a, b;
	cin >> a >> b;
	
	cout << (a + b) << "\n"; //somma
	cout << (a - b) << "\n"; //differenza
	cout << (a * b) << "\n"; //prodotto
	cout << (a / b) << "\n"; //quoziente
	cout << (a % b) << "\n"; //modulo
	cout << "\n";
	cout << (a > b) << "\n"; //maggiore
	cout << (a < b) << "\n"; //minore
	cout << (a == b) << "\n"; //uguale
}
